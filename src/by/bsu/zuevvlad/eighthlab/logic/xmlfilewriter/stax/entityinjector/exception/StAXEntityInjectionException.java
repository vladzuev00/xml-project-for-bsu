package by.bsu.zuevvlad.eighthlab.logic.xmlfilewriter.stax.entityinjector.exception;

public final class StAXEntityInjectionException extends Exception
{
    public StAXEntityInjectionException()
    {
        super();
    }

    public StAXEntityInjectionException(final String description)
    {
        super(description);
    }

    public StAXEntityInjectionException(final Exception cause)
    {
        super(cause);
    }

    public StAXEntityInjectionException(final String description, final Exception cause)
    {
        super(description, cause);
    }

}
